s='Hello, World!'
print(s[2:5]) #slicing
print(s[:5]) #print literal till index 5
print(s[2:]) #print from index 2 to end
print(s[-5:-2]) #negative indexing
print(s.upper()) #convert to uppercase
print(s.lower()) #convert to lowercase
print(s.strip()) #remove whitespace
print(len(s)) #length of string
print(s.replace("H","J")) #replace H with J
print(s.split(",")) #split string at comma